////////////////////////// Data sanitize  //////////////////////////////
////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

function sanitize($con, $data)
{
    return mysqli_real_escape_string($con, $data);
}

////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////
////////////////////////// Image Validation ////////////////////////////
////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

function isValidImg($file)
{
    $allow = ["jpg", "JPG", "png", "PNG", "jpeg", "JPEG", "webp", "WEBP"];
    $ext = end(explode(".", $file));
    if (in_array($ext, $allow)) {
        return true;
    } else {
        return false;
    }
}
 